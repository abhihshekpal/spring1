package com.tcgi.springcoree;

public class Student {

	String studName;
	int StudRoll;
	public String getStudName() {
		return studName;
	}
	public void setStudName(String studName) {
		this.studName = studName;
	}
	public int getStudRoll() {
		return StudRoll;
	}
	public void setStudRoll(int studRoll) {
		StudRoll = studRoll;
	}
}
